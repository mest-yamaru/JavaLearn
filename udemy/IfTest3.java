public class IfTest3 {
    public static void main(String[] args) {
        int myAge = 20;
        if(myAge == 35) {
            System.out.println("私の年齢は３５歳です！");
        } else if(myAge >= 40) {
            System.out.println("私の年齢は４０歳以上です！");
        } else {
            System.out.println("私の年齢は３５歳でも、４０歳以上でもないです");
        }
        System.out.println("私の出身地は・・・");
    }
}
